# 2. **Data Type Play**: Create variables of each data type (integer, float, string, boolean, list, tuple, dictionary, set) and print their types and values.
#     - *Input*: None
#     - *Output*: "Type of variable1: <class 'int'>, value: 10..."



# Integer
integer_variable = 42

# Float
float_variable = 3.14

# String
string_variable = "Hello, Python!"

# Boolean
boolean_variable = True

# List
list_variable = [1, 2, 3, 4, 5]

# Tuple
tuple_variable = (10, 20, 30)

# Dictionary
dictionary_variable = {"name": "John", "age": 30, "city": "New York"}

# Set
set_variable = {1, 2, 3, 4, 5}

# Printing types and values
print("Integer - Type:", type(integer_variable), ", Value:", integer_variable)
print("Float - Type:", type(float_variable), ", Value:", float_variable)
print("String - Type:", type(string_variable), ", Value:", string_variable)
print("Boolean - Type:", type(boolean_variable), ", Value:", boolean_variable)
print("List - Type:", type(list_variable), ", Value:", list_variable)
print("Tuple - Type:", type(tuple_variable), ", Value:", tuple_variable)
print("Dictionary - Type:", type(dictionary_variable), ", Value:", dictionary_variable)
print("Set - Type:", type(set_variable), ", Value:", set_variable)
